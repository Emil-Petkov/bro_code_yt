import math

number = 17.23

print(math.ceil(number))  # 18
print(math.floor(number))  # 17
print(abs(-number))  # -17.23 -> 17.23
print(math.sqrt(2))  # Коренът от 2 -> 1.4142135623730951
print(math.pow(number, 7))  # 450813530.3560035
print(round(number))  # 17.23 = 17, but 17.50...51 = 18
print(max([1, 2, 3]))  # max number is 3
print(min([1, -2, 3]))  # min number is -2
print(sum([1, 2, 3]))  # sum of numbers equal to 6
